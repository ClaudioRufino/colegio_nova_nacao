package Modelo;
public class Pessoa {
    private String nome;
    private int BI;
    private String sexo;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getBI() {
        return BI;
    }

    public void setBI(int BI) {
        this.BI = BI;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }
    
}
