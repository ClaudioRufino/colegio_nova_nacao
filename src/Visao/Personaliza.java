    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here
        
        if(!txt_mudar_nome.getText().isBlank()){
            sistema.setNomeEscola(txt_mudar_nome.getText());
            sistemaDao.alterarNome(sistema);
            principal.nome_escola.setText(txt_mudar_nome.getText());
        }
        
        if(radio_preto.isSelected()){
            sistema.setCor("preto");
        }
        else if(radio_azul.isSelected()){
            sistema.setCor("azul");
        }
        else if(radio_padrao.isSelected()){
            sistema.setCor("padrao");
        }
        if(mudanca){
            
            sistemaDao.alterarCor(sistema);
            principal.painel_frontal_cima.setBackground(sistemaDao.corSistema());
            principal.painel_frontal_baixo.setBackground(sistemaDao.corSistema());
            
            JOptionPane.showMessageDialog(null, "Alterações guardadas. O sistema tem que ser reiniciado"
                    + " para veres as alterações!");
            this.dispose();
            System.exit(0);
        }
        else{
            JOptionPane.showMessageDialog(null, "Não houve nenhuma alteração!");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

