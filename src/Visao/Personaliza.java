    private void radio_padraoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_radio_padraoItemStateChanged
        // TODO add your handling code here:
        if(radio_padrao.isSelected() ){
            radio_preto.setSelected(false);
            radio_azul.setSelected(false);
            mudanca = true;
        }
        else if(!radio_preto.isSelected() && !radio_azul.isSelected()){
            radio_padrao.setSelected(true);
        }
    }//GEN-LAST:event_radio_padraoItemStateChanged

